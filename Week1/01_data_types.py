"""
Week 1: Data Types Examples
Topics: int, float, string, boolean
"""

# Integer examples
age = 25
print(f"Age: {age}, Type: {type(age)}")

# Float examples
temperature = 98.6
print(f"Temperature: {temperature}, Type: {type(temperature)}")

# String examples
name = "Python Programming"
print(f"Name: {name}, Type: {type(name)}")
print(f"Length: {len(name)}")

# Boolean examples
is_student = True
is_graduated = False
print(f"Is Student: {is_student}, Type: {type(is_student)}")

# Type conversion
num_str = "42"
num_int = int(num_str)
print(f"String '{num_str}' converted to int: {num_int}")

price = 19.99
price_int = int(price)
print(f"Float {price} converted to int: {price_int}")
