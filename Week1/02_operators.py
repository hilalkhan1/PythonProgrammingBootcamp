"""
Week 1: Operators Examples
Topics: Arithmetic, Comparison, Logical operators
"""

# Arithmetic Operators
a = 15
b = 4

print("=== Arithmetic Operators ===")
print(f"Addition: {a} + {b} = {a + b}")
print(f"Subtraction: {a} - {b} = {a - b}")
print(f"Multiplication: {a} * {b} = {a * b}")
print(f"Division: {a} / {b} = {a / b}")
print(f"Floor Division: {a} // {b} = {a // b}")
print(f"Modulus: {a} % {b} = {a % b}")
print(f"Exponentiation: {a} ** 2 = {a ** 2}")

# Comparison Operators
print("\n=== Comparison Operators ===")
x = 10
y = 20

print(f"{x} == {y}: {x == y}")
print(f"{x} != {y}: {x != y}")
print(f"{x} > {y}: {x > y}")
print(f"{x} < {y}: {x < y}")
print(f"{x} >= {y}: {x >= y}")
print(f"{x} <= {y}: {x <= y}")

# Logical Operators
print("\n=== Logical Operators ===")
is_adult = True
has_id = False

print(f"is_adult AND has_id: {is_adult and has_id}")
print(f"is_adult OR has_id: {is_adult or has_id}")
print(f"NOT is_adult: {not is_adult}")
